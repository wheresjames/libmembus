#!/usr/bin/env python3
import json
import math
import os
import sys


def fmt_num(value):
    if value >= 1_000_000:
        return f"{value / 1_000_000:.2f}M"
    if value >= 1_000:
        return f"{value / 1_000:.2f}K"
    return f"{value:.0f}"


def svg_escape(text):
    return (
        str(text)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def bar_chart(results, path, metric, title, unit):
    width = 1100
    height = 560
    margin_left = 260
    margin_right = 40
    margin_top = 70
    bar_h = 34
    gap = 18
    plot_w = width - margin_left - margin_right
    max_v = max((r[metric] for r in results), default=1.0)
    max_v = max(max_v, 1.0)
    chart_h = margin_top + len(results) * (bar_h + gap) + 60
    height = max(height, chart_h)

    colors = {
        "memmap": "#60a5fa",
        "memmsg": "#34d399",
        "memcmd": "#a78bfa",
        "memkv": "#fb7185",
        "memvid": "#fb923c",
        "memaud": "#22d3ee",
    }

    lines = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}">',
        '<rect width="100%" height="100%" fill="#0f172a"/>',
        f'<text x="{width / 2}" y="36" text-anchor="middle" font-family="Arial, sans-serif" font-size="24" font-weight="700" fill="#f8fafc">{svg_escape(title)}</text>',
        f'<line x1="{margin_left}" y1="{margin_top - 15}" x2="{margin_left + plot_w}" y2="{margin_top - 15}" stroke="#334155"/>',
    ]

    for i, r in enumerate(results):
        y = margin_top + i * (bar_h + gap)
        value = r[metric]
        bar_w = value / max_v * plot_w
        label = f'{r["name"]} ({r["payload"]})'
        color = colors.get(r["group"], "#4b5563")
        lines.append(f'<text x="{margin_left - 12}" y="{y + 23}" text-anchor="end" font-family="Arial, sans-serif" font-size="14" fill="#cbd5e1">{svg_escape(label)}</text>')
        lines.append(f'<rect x="{margin_left}" y="{y}" width="{bar_w:.2f}" height="{bar_h}" rx="4" fill="{color}"/>')
        value_label = f"{fmt_num(value)} {unit}"
        if bar_w > plot_w - 130:
            lines.append(f'<text x="{margin_left + bar_w - 8}" y="{y + 23}" text-anchor="end" font-family="Arial, sans-serif" font-size="14" font-weight="700" fill="#0f172a">{svg_escape(value_label)}</text>')
        else:
            lines.append(f'<text x="{margin_left + bar_w + 8}" y="{y + 23}" font-family="Arial, sans-serif" font-size="14" fill="#e2e8f0">{svg_escape(value_label)}</text>')

    lines.append(f'<text x="{margin_left}" y="{height - 20}" font-family="Arial, sans-serif" font-size="12" fill="#94a3b8">Higher is better. Generated from bench-libmembus JSON output.</text>')
    lines.append("</svg>")
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


def write_summary(data, path):
    meta = data["metadata"]
    rows = data["results"]
    lines = [
        "| Benchmark | Payload | Ops/sec | MiB/sec | ns/op |",
        "|---|---:|---:|---:|---:|",
    ]
    for r in rows:
        lines.append(
            f'| {r["name"]} | {r["payload"]} | {fmt_num(r["ops_per_sec"])} | '
            f'{r["mb_per_sec"]:.1f} | {r["ns_per_op"]:.1f} |'
        )

    text = [
        "<!-- Generated by bench/plot_results.py. Do not edit by hand. -->",
        "",
        f'Benchmark duration: `{meta["duration_ms"]} ms` per case',
        "",
        f'System: `{meta.get("system", "unknown")}`',
        "",
        *lines,
        "",
    ]
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(text))


def main():
    if len(sys.argv) != 3:
        print("usage: plot_results.py input.json output_dir", file=sys.stderr)
        return 2

    input_path = sys.argv[1]
    output_dir = sys.argv[2]
    os.makedirs(output_dir, exist_ok=True)

    with open(input_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    results = data["results"]
    bar_chart(results, os.path.join(output_dir, "throughput_ops.svg"), "ops_per_sec", "libmembus Operations Throughput", "ops/s")
    bar_chart(results, os.path.join(output_dir, "throughput_mib.svg"), "mb_per_sec", "libmembus Data Throughput", "MiB/s")
    write_summary(data, os.path.join(output_dir, "summary.md"))
    print(f"wrote {output_dir}/summary.md")
    print(f"wrote {output_dir}/throughput_ops.svg")
    print(f"wrote {output_dir}/throughput_mib.svg")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
